package com.example.demo.entity;



import java.util.Objects;

public class Kniga {

    private Long id;
    private String name;
    private String email;
    private Long phone;


    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Long getPhone() {
        return phone;
    }

    public void setPhone(Long phone) {
        this.phone = phone;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Kniga kniga = (Kniga) o;
        return Objects.equals(id, kniga.id) &&
                Objects.equals(name, kniga.name) &&
                Objects.equals(email, kniga.email) &&
                Objects.equals(phone, kniga.phone);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, name, email, phone);
    }

    @Override
    public String toString() {
        return "com.example.demo.entity.Kniga{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", email='" + email + '\'' +
                ", phone='" + phone + '\'' +
                '}';
    }
}
